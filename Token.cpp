//==============================================================================
//! @file Token.cpp
//==============================================================================
#include "Token.h"

//------------------------------------------------------------------------------
// Constructor
//------------------------------------------------------------------------------
Token::Token()
    :mType(Others)
    ,mText("")
    ,mIntValue(0)
{
}

//------------------------------------------------------------------------------
// Constructor with arguments
//------------------------------------------------------------------------------
Token::Token(TokenType type, const std::string & text, int intValue)
    :mType(type)
    ,mText(text)
    ,mIntValue(intValue)
{
}

//------------------------------------------------------------------------------
// Get type
//------------------------------------------------------------------------------
TokenType Token::type()const
{
    return mType;
}

//------------------------------------------------------------------------------
// Get text
//------------------------------------------------------------------------------
const std::string & Token::text()const
{
    return mText;
}

//------------------------------------------------------------------------------
// Get intValue
//------------------------------------------------------------------------------
int Token::intValue()const
{
    return mIntValue;
}
